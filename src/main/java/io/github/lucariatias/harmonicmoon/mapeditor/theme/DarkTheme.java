public class DarkTheme extends Theme {

    public DarkTheme() {
        setName("Dark");
        setBackgroundColour(new Color(15, 15, 15));
        setContainerBackgroundColour(new Color(23, 23, 23));
        setBoxUpBackgroundColour(new Color(52, 52, 52));
        setBoxHoverBackgroundColour(new Color(60, 60, 60));
        setBoxDownBackgroundColour(new Color(44, 44, 44));
        setBoxUpEdgeColour(new Color(77, 77, 77));
        setBoxHoverEdgeColour(new Color(93, 93, 93));
        setBoxDownEdgeColour(new Color(61, 61, 61));
        setBoxUpTextColour(new Color(125, 125, 125));
        setBoxHoverTextColour(new Color(189, 189, 189));
        setBoxDownTextColour(new Color(61, 61, 61));
    }
}
